	 <h3>Brief history of Didcy</h3>
	 <p>It was in the year 2021, 1st of August where a young computer programmer decided to create or build a digital version of 
	 a real world chain of stores along the roadside. In his own words, he says <q>I want to build a digital chain of stores that will be owned by my company like in the real world 
	 but instead of renting out all as empty stores to business tenants for business operations why don't I define each store 
	 myself and leave the last store empty for tenants to define it themselves and then rent them all out to businesses and individuals for absolutely <b><i style="color:red">free</i></b> of charge.</q>
	 And so he began his build. 
	 </p>
	 <p>The digital platform was launched on September 1st, 2022 but currently experience technical(so we're around the clock to get it fixed) with a team of smart developers and designers. The platform was named <q><b><i>Didcy</i></b></q>, because it had exhausted all naming combinations. So Didcy was born.</p>
	 <p>Didcy was founded by three great developers, namely, <b style="color: rgb(120, 22, 255)">David Ansong, David Quartey and Alex Sanchez</b>. But David Ansong still remain the grand founder and CEO of Didcy.</p>
	 <p>Didcy as defined earlier by it's grand founder and CEO, is a platform designed to house a chain of digitally defined stores with the exception of one. Didcy was created to careter for all kind of users, visitors or customers like the way a 
	 real world chain of stores would be defined to. We call it spaces instead of stores. Hence, we say <b><i>Didcy Spaces.</i></b></p>
